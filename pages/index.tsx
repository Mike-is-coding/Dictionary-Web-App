import Head from "next/head";
import { useEffect, useState } from "react";
import QuerySection from "./QuerySection";
import Content from "./Content";
import { DictionaryData } from "./DictionaryInterfaces";

export default function Home() {
  const [theme, setTheme] = useState<string>("");
  const [font, setFont] = useState<string>("font-sans");
  const [userWord, setUserWord] = useState<string>("");
  const [dictData, setDictData] = useState<DictionaryData>({
    word: "",
    phonetic: "",
    phonetics: [{}],
    origin: "",
    meanings: [{}]
  });

  useEffect(() => {
    if (theme === "dark") {
      document.body.classList.add('bg-black');
    } else {
      document.body.classList.remove('bg-black');
      document.body.classList.add('bg-white')
    }
  })
 

  return (
    <>
      <Head>
        <title>Dictionary</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
      </Head>
      <main className={`flex ${font} ${theme} w-11/12 md:w-3/4 lg:w-2/4 h-fit mx-auto justify-center flex-col`} id="Content-container">
        <QuerySection
          font={font}
          setFont={setFont}
          userWord={userWord}
          setUserWord={setUserWord}
          dictData={dictData}
          setDictData={setDictData}
          theme={theme}
          setTheme={setTheme}
        />
        <Content 
        dictData={dictData}
        userWord={userWord}
        />
      </main>
    </>
  );
}
